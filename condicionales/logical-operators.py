#and
resultado = True & True
resultado2 = True & False
resultado3 = False & True
resultado4 = False & False
tupla_and = (resultado,resultado2,resultado3,resultado4)
print(tupla_and)

#or 
resultado = True | True
resultado2 = True | False
resultado3 = False | True
resultado4 = False | False
tupla_or = (resultado,resultado2,resultado3,resultado4)
print(tupla_or)

#not
resultado = not True
resultado2 = not False 
tupla_not = (resultado,resultado2)
print (tupla_not)